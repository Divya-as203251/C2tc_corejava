package com.user.Abstraction;

public class Rectangle extends Shape {
	private float width,height;

	public Rectangle() {
		super();
		this.width =0.5f;
		this.height =0.02f;
	}

	public Rectangle(float width, float height) {
		super();
		this.width = width;
		this.height = height;
	}
	

}
